    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                    String user = txtusername.getText();
                    String prez = txtprezime.getText();
                    String mail = txtmail.getText();
                    String pas1 = jtxt1.getText();
                    String pas2 = jtxt2.getText();
       
                    try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/linda","root","");   
            insert = con1.prepareStatement("insert into reg(Ime,Prezime,Lozinka,Mail)values(?,?,?,?)");
            
                    
                    
                  
            
                    if(user.isEmpty()&&prez.isEmpty()&&mail.isEmpty()&&pas1.isEmpty()&&pas2.isEmpty() ){
                        JOptionPane.showMessageDialog(this, "Krivi unos podataka, poku≈°aj ponovo");
                        txtusername.setText("");
                        txtprezime.setText("");
                        txtmail.setText("");
                        jtxt1.setText("");
                        jtxt2.setText("");
                        
                    }
                    else{
                    
                        insert.setString(1, user);
                    insert.setString(2, prez);
                    insert.setString(3, pas1);
                    insert.setString(4, mail);
                    insert.executeUpdate();
                       JOptionPane.showMessageDialog(rootPane, "Uspje≈°na registracija");

                    Java_MysqlConn m = new Java_MysqlConn();
                    this.hide();
                    m.setVisible(true);
             }
        
        } 

        catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
     
    }//GEN-LAST:event_jButton1ActionPerformed

